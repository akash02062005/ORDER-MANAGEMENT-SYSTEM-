import { StrictMode } from 'react'
import { createRoot } from 'react-dom/client'
import { QueryClient, QueryClientProvider } from '@tanstack/react-query'
import './index.css'
import App from './App.jsx'

// Single shared React Query client. Tuning notes:
// - staleTime 30s keeps the UI snappy without hammering the API.
// - gcTime 5min means freshly visited tabs don't refetch from scratch.
// - refetchOnWindowFocus false because realtime updates flow over WebSocket;
//   pulling on focus would cause double work.
const queryClient = new QueryClient({
  defaultOptions: {
    queries: {
      staleTime: 30_000,
      gcTime: 5 * 60_000,
      refetchOnWindowFocus: false,
      retry: 1,
    },
    mutations: { retry: 0 },
  },
})

createRoot(document.getElementById('root')).render(
  <StrictMode>
    <QueryClientProvider client={queryClient}>
      <App />
    </QueryClientProvider>
  </StrictMode>,
)
